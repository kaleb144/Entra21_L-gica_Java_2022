package br.com.entra21.PráticaOperadorIgualdade;

public class Main {

	public static void main(String[] args) {
		
		System.out.println("Prática operador igualdade. \n");
		
		byte n1,n2;
		
		boolean resultado;
		
		n1=2;
		n2=4;
		
		resultado = n1==n2;
		
		System.out.println("Ao comparar a igualdade resultou em ? " +resultado);
		
		resultado = n1 != n2;
		
		System.out.println("Ao comparar a desigualdade resultou em ? " +resultado);		

	}

}
